public class Coppia {
    private int ele1;
    private int ele2;

    public Coppia(int ele1, int ele2) {
        this.ele1 = ele1;
        this.ele2 = ele2;
    }

    public int getEle1() {
        return ele1;
    }

    public void setEle1(int ele1) {
        this.ele1 = ele1;
    }

    public int getEle2() {
        return ele2;
    }

    public void setEle2(int ele2) {
        this.ele2 = ele2;
    }
    
    
}
